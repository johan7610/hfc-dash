    Phase D5 — Strategic weekly brief.

    Narrative comes from Anthropic Sonnet 4.6 via AnthropicGateway (handled
    by StrategicBriefService). $brief shape:
      narrative_text  string (AI or templated fallback — plain text)
      narrative_html  string (legacy templated HTML — kept for back-compat)
      generated_at    Carbon
      from_cache      bool
      from_fallback   bool   (true when API failed / disabled / capped)
