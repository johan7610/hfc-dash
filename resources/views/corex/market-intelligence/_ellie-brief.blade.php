    if ($narrative === '' && !empty($b['narrative_html'])) {
        // Legacy callers still passing HTML — strip tags so this view stays
        // in the plain-text branch.
        $narrative = trim(strip_tags($b['narrative_html']));
    }
    $sourceLabel = $b['from_fallback']
        ? 'fallback copy (AI unavailable)'
        : ($b['from_cache'] ? 'cached' : 'fresh');
