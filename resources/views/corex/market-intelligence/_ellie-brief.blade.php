    $b = $brief ?? [
        'narrative_text' => '',
        'narrative_html' => '',
        'generated_at'   => null,
        'from_cache'     => false,
        'from_fallback'  => false,
        'actions'        => [],
    ];
    $generatedAt = $b['generated_at'] instanceof \Carbon\Carbon
        ? $b['generated_at']
        : ($b['generated_at'] ? \Carbon\Carbon::parse($b['generated_at']) : null);
